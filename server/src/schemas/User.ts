import mongoose, { model, Schema } from "mongoose";
import { ISignupForm } from "../interfaces/IAuth";

const userSchema = new Schema({
  name: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
    unique: true,
  },
  password: {
    type: String,
    required: true,
  },
  createdAt: {
    type: Date,
    default: Date.now(),
  },
});

export default model<ISignupForm>("users", userSchema);
